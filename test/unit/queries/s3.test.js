import 'mocha';
import { expect } from 'chai';
import sinon from 'sinon';
import _ from 'highland';
import { Readable } from 'stream';

import {
  getObjectFromS3,
  listObjectsFromS3,
  pageObjectsFromS3,
  splitS3Object,
  toGetObjectRequest,
  toGetObjectRequest2,
  getObjectFromS3AsStream,
  headS3Object,
} from '../../../src/queries/s3';

import Connector from '../../../src/connectors/s3';

describe('queries/s3.js', () => {
  afterEach(sinon.restore);

  it('should get object', (done) => {
    const stub = sinon.stub(Connector.prototype, 'getObject').resolves({
      Body: JSON.stringify({ f1: 'v1' }),
    });

    const uows = [{
      getRequest: {
        Key: 'k1',
      },
    }];

    _(uows)
      .through(getObjectFromS3())
      .collect()
      .tap((collected) => {
        // console.log(JSON.stringify(collected, null, 2));

        expect(stub).to.have.been.calledWith({
          Key: 'k1',
        });

        expect(collected.length).to.equal(1);
        expect(collected[0]).to.deep.equal({
          getRequest: {
            Key: 'k1',
          },
          getResponse: {
            Body: JSON.stringify({ f1: 'v1' }),
          },
        });
      })
      .done(done);
  });

  it('should get and split object', (done) => {
    sinon.stub(Connector.prototype, 'getObject').resolves(GET_RESPONSE);

    const uows = [{
      record: {
        s3: {
          bucket: {
            name: 'event-lake-s3-stg-bucket-1vdisrg5i6pxt',
          },
          object: {
            key: 'us-east-1/2020/04/22/19/event-lake-s3-stg-DeliveryStream-1A6OO953Z8EU4-1-2020-04-22-19-16-13-013a227d-d7fe-47e9-878a-97763ce279b7',
          },
        },
      },
    }];

    _(uows)
      .map(toGetObjectRequest)
      .through(getObjectFromS3())
      .flatMap(splitS3Object())
      .map((uow) => {
        const { detail, ...eb } = JSON.parse(uow.getResponse.line);
        return ({
          ...uow,
          record: {
            ...uow.record,
            eb,
          },
          event: detail,
        });
      })
      .collect()
      .tap((collected) => {
        // console.log(JSON.stringify(collected, null, 2));

        expect(collected.length).to.equal(3);

        // console.log(JSON.stringify(collected[0], null, 2));

        expect(collected[0].event.type).to.equal('thing-created');
      })
      .done(done);
  });

  it('should get object as stream', (done) => {
    const objectStream = new Readable();
    objectStream.push('name,color\nBob,Red\nAlice,Yellow'); // data in the stream
    objectStream.push(null); // end of file

    const stub = sinon.stub(Connector.prototype, 'getObjectStream').returns(Promise.resolve(objectStream));

    const uows = [{
      getRequest: {
        Key: 'k1',
      },
    }];

    _(uows)
      .through(getObjectFromS3AsStream())
      .collect()
      .tap((collected) => {
        // console.log(JSON.stringify(collected, null, 2));

        expect(stub).to.have.been.calledWith({
          Key: 'k1',
        });

        expect(collected.length).to.equal(3);
        expect(collected[0]).to.deep.equal({
          getRequest: {
            Key: 'k1',
          },
          getResponse: 'name,color',
        });
        expect(collected[1]).to.deep.equal({
          getRequest: {
            Key: 'k1',
          },
          getResponse: 'Bob,Red',
        });
        expect(collected[2]).to.deep.equal({
          getRequest: {
            Key: 'k1',
          },
          getResponse: 'Alice,Yellow',
        });
      })
      .done(done);
  });

  it('should get object as stream missing getRequestField', (done) => {
    const stub = sinon.stub(Connector.prototype, 'getObjectStream').returns('');
    const uows = [{
      // getRequestField: 'missing',
    }];

    _(uows)
      .through(getObjectFromS3AsStream())
      .collect()
      .tap((collected) => {
        // console.log(JSON.stringify(collected, null, 2));
        expect(stub).to.have.not.been.called;
        expect(collected.length).to.equal(1);
        expect(collected[0]).to.deep.equal({});
      })
      .done(done);
  });

  it('should get from s3 sns', (done) => {
    const uows = [{
      record: {
        s3: {
          s3: {
            bucket: {
              name: 'b1',
            },
            object: {
              key: 'k1',
            },
          },
        },
      },
    }];

    _(uows)
      .map(toGetObjectRequest2)
      .collect()
      .tap((collected) => {
        // console.log(JSON.stringify(collected, null, 2));
        expect(collected.length).to.equal(1);
        expect(collected[0].getRequest).to.deep.equal({
          Bucket: 'b1',
          Key: 'k1',
        });
      })
      .done(done);
  });

  it('should list objects', (done) => {
    const stub = sinon.stub(Connector.prototype, 'listObjects').resolves({
      IsTruncated: false,
      NextContinuationToken: '',
      Contents: [
        {
          Key: 'p1/2021/03/26/19/1234',
        },
      ],
    });

    const uows = [{
      listRequest: {
        Prefix: 'p1',
      },
    }];

    _(uows)
      .through(listObjectsFromS3())
      .collect()
      .tap((collected) => {
        // console.log(JSON.stringify(collected, null, 2));

        expect(stub).to.have.been.calledWith({
          Prefix: 'p1',
        });

        expect(collected.length).to.equal(1);
        expect(collected[0]).to.deep.equal({
          listRequest: {
            Prefix: 'p1',
          },
          listResponse: {
            IsTruncated: false,
            NextContinuationToken: '',
            Contents: [
              {
                Key: 'p1/2021/03/26/19/1234',
              },
            ],
          },
        });
      })
      .done(done);
  });

  it('should page objects', (done) => {
    const responses = [
      {
        IsTruncated: true,
        NextContinuationToken: 'm1',
        Contents: [
          {
            Key: 'p1/2021/03/26/19/1234',
          },
          {
            Key: 'p1/2021/03/26/19/5678',
          },
        ],
      },
      {
        IsTruncated: false,
        NextContinuationToken: undefined,
        Contents: [
          {
            Key: 'p1/2021/03/26/19/9012',
          },
        ],
      },
    ];

    const stub = sinon.stub(Connector.prototype, 'listObjects')
      .callsFake(() => Promise.resolve((responses.shift())));

    const uows = [{
      listRequest: {
        Prefix: 'p1',
      },
    }];

    _(uows)
      .through(pageObjectsFromS3())
      .collect()
      .tap((collected) => {
        // console.log(JSON.stringify(collected, null, 2));

        expect(stub).to.have.been.calledWith({
          Prefix: 'p1',
          ContinuationToken: undefined,
        });
        expect(stub).to.have.been.calledWith({
          Prefix: 'p1',
          ContinuationToken: 'm1',
        });

        expect(collected.length).to.equal(3);
        expect(collected[0]).to.deep.equal({
          listRequest: {
            Prefix: 'p1',
            ContinuationToken: undefined,
          },
          listResponse: {
            IsTruncated: true,
            NextContinuationToken: 'm1',
            Content: {
              Key: 'p1/2021/03/26/19/1234',
            },
          },
        });
        expect(collected[1]).to.deep.equal({
          listRequest: {
            Prefix: 'p1',
            ContinuationToken: undefined,
          },
          listResponse: {
            IsTruncated: true,
            NextContinuationToken: 'm1',
            Content: {
              Key: 'p1/2021/03/26/19/5678',
            },
          },
        });
        expect(collected[2]).to.deep.equal({
          listRequest: {
            Prefix: 'p1',
            ContinuationToken: 'm1',
          },
          listResponse: {
            IsTruncated: false,
            NextContinuationToken: undefined,
            Content: {
              Key: 'p1/2021/03/26/19/9012',
            },
          },
        });
      })
      .done(done);
  });
  it('should head object', (done) => {
    const stub = sinon.stub(Connector.prototype, 'headObject').resolves({
      Metadata: {
        testkey: '1',
      },
    });

    const uows = [{
      headRequest: {
        Key: 'k1',
      },
    }];

    _(uows)
      .through(headS3Object())
      .collect()
      .tap((collected) => {
        console.log(JSON.stringify(collected, null, 2));

        expect(stub).to.have.been.calledWith({
          Key: 'k1',
        });

        expect(collected.length).to.equal(1);
        expect(collected[0]).to.deep.equal({
          headRequest: {
            Key: 'k1',
          },
          headResponse: {
            Metadata: {
              testkey: '1',
            },
          },
        });
      })
      .done(done);
  });
  it('should head object missing headRequestField', (done) => {
    const stub = sinon.stub(Connector.prototype, 'headObject').resolves({
      Metadata: {
        testkey: '1',
      },
    });

    const uows = [{
      // headRequest: {
      //   Key: 'k1',
      // },
    }];

    _(uows)
      .through(headS3Object())
      .collect()
      .tap((collected) => {
        // console.log(JSON.stringify(collected, null, 2));

        expect(collected[0]).to.deep.equal({});

        expect(collected.length).to.equal(1);
      })
      .done(done);
  });
});

